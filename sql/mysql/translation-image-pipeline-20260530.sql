SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

CREATE TABLE IF NOT EXISTS `image_translation_task` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '图片翻译任务编号',
  `user_id` BIGINT UNSIGNED NOT NULL COMMENT '用户编号',
  `task_no` VARCHAR(64) NOT NULL COMMENT '任务号',
  `trace_id` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '链路追踪编号',
  `source_lang` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '源语言',
  `target_lang` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '目标语言',
  `mode` VARCHAR(32) NOT NULL DEFAULT 'AUTO' COMMENT '任务模式：AUTO/FAST/HIGH_QUALITY',
  `prefer_provider` VARCHAR(64) NOT NULL DEFAULT 'auto' COMMENT '偏好服务商',
  `status` VARCHAR(32) NOT NULL DEFAULT 'PENDING' COMMENT 'PENDING/PROCESSING/SUCCESS/FAILED/DEGRADED',
  `display_mode` VARCHAR(32) NULL COMMENT 'FINAL_IMAGE/APP_RENDERED_OVERLAY/TEXT_LIST/FAILED',
  `cached` TINYINT NOT NULL DEFAULT 0 COMMENT '是否缓存命中',
  `raw_sha256` CHAR(64) NOT NULL COMMENT '原图 SHA-256',
  `cache_key` CHAR(64) NOT NULL COMMENT '缓存键',
  `original_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '原图 COS key',
  `enhanced_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '增强图 COS key',
  `result_image_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '译图 COS key',
  `result_json_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '结果 JSON COS key',
  `quality_json_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '质量检测 JSON COS key',
  `text_items_json` MEDIUMTEXT NULL COMMENT '脱敏后的文本框结果 JSON',
  `provider` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '服务商',
  `provider_task_id` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '服务商任务编号',
  `provider_request_id` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '服务商请求编号',
  `quality_score` DECIMAL(5,4) NULL COMMENT '质量分',
  `fail_reason` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '失败原因',
  `warning_message` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '用户提示',
  `finished_at` DATETIME NULL COMMENT '完成时间',
  `expire_at` DATETIME NULL COMMENT '结果过期时间',
  `creator` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '创建者',
  `create_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updater` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '更新者',
  `update_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '是否删除',
  `tenant_id` BIGINT NOT NULL DEFAULT 0 COMMENT '租户编号',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_image_translation_task_no` (`task_no`),
  KEY `idx_image_translation_user_time` (`user_id`, `create_time`),
  KEY `idx_image_translation_cache_status` (`cache_key`, `status`, `deleted`),
  KEY `idx_image_translation_expire` (`expire_at`),
  KEY `idx_image_translation_provider_task` (`provider`, `provider_task_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='图片翻译任务';

CREATE TABLE IF NOT EXISTS `image_translation_cache` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '图片翻译缓存编号',
  `cache_key` CHAR(64) NOT NULL COMMENT '缓存键',
  `raw_sha256` CHAR(64) NOT NULL COMMENT '原图 SHA-256',
  `source_lang` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '源语言',
  `target_lang` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '目标语言',
  `provider` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '服务商',
  `pipeline_version` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '流水线版本',
  `preprocess_version` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '预处理版本',
  `render_version` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '渲染版本',
  `display_mode` VARCHAR(32) NOT NULL DEFAULT '' COMMENT '展示模式',
  `result_image_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '译图 COS key',
  `result_json_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '结果 JSON COS key',
  `quality_json_cos_key` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '质量 JSON COS key',
  `text_items_json` MEDIUMTEXT NULL COMMENT '脱敏后的文本框结果 JSON',
  `quality_score` DECIMAL(5,4) NULL COMMENT '质量分',
  `warning_message` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '用户提示',
  `hit_count` INT NOT NULL DEFAULT 0 COMMENT '命中次数',
  `expire_at` DATETIME NULL COMMENT '过期时间',
  `creator` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '创建者',
  `create_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updater` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '更新者',
  `update_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '是否删除',
  `tenant_id` BIGINT NOT NULL DEFAULT 0 COMMENT '租户编号',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_image_translation_cache_key` (`cache_key`),
  KEY `idx_image_translation_cache_lookup` (`raw_sha256`, `source_lang`, `target_lang`, `provider`),
  KEY `idx_image_translation_cache_expire` (`expire_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='图片翻译缓存';

CREATE TABLE IF NOT EXISTS `image_translation_provider_log` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '图片翻译服务商日志编号',
  `task_id` BIGINT UNSIGNED NOT NULL COMMENT '任务编号',
  `provider` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '服务商',
  `request_id` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '请求编号',
  `provider_task_id` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '服务商任务编号',
  `request_payload_digest` CHAR(64) NOT NULL DEFAULT '' COMMENT '请求摘要',
  `response_payload_digest` CHAR(64) NOT NULL DEFAULT '' COMMENT '响应摘要',
  `http_status` INT NULL COMMENT 'HTTP 状态',
  `error_code` VARCHAR(128) NOT NULL DEFAULT '' COMMENT '错误码',
  `error_message` VARCHAR(512) NOT NULL DEFAULT '' COMMENT '错误信息',
  `cost_ms` BIGINT NOT NULL DEFAULT 0 COMMENT '耗时毫秒',
  `creator` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '创建者',
  `create_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updater` VARCHAR(64) NOT NULL DEFAULT '' COMMENT '更新者',
  `update_time` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  `deleted` TINYINT NOT NULL DEFAULT 0 COMMENT '是否删除',
  `tenant_id` BIGINT NOT NULL DEFAULT 0 COMMENT '租户编号',
  PRIMARY KEY (`id`),
  KEY `idx_image_translation_provider_log_task` (`task_id`, `create_time`),
  KEY `idx_image_translation_provider_log_request` (`provider`, `request_id`),
  KEY `idx_image_translation_provider_log_provider_task` (`provider`, `provider_task_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='图片翻译服务商调用日志';

SET FOREIGN_KEY_CHECKS = 1;
